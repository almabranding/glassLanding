            <h1>TERRA GROUP</h1>
            <p>The Developer</p>

            <p>Terra Group formed in 2001, with the ambition to evolve the Miami landscape - and by extension, the lives of the people within it. Terra Group’s founder, Pedro Martin, has spent his entire professional life in South Florida, subsequently recognizing the opportunity to enrich and invigorate Miami by creating truly original, exceptional spaces in unexpected places. Together with his son, David, Terra Group has made it their mission to refine (and redefine) the way people could and should live, beyond the norm.</p>

            <p>With an in-house team of the very best people in their field – architects, builders, lawyers, designers - David and Pedro have quickly gained a reputation for creating highly original, high-end residential, commercial and mixed-used projects. However, unlike other developers – and as Miami residents themselves – Pedro and David do not simply buy up unwanted pieces of real estate with the hope of giving them a facelift. (After all, that is not progress). Instead, they choose only to work on projects that speak to their imaginations, and to their vision of a new Miami: one which helps people, and places to unlock or realize their full potential. As each project is carefully selected with an individual plan in mind, Terra Group ensures that their work makes a lasting contribution, not just to the landscape, but to the culture of Miami as a whole.</p>

            <p>By following their evolutionary instinct to this day, Pedro and David continue to find new opportunities, creating enriching (not to mention award-winning) connections between people and places. With an
            active involvement in South Florida’s charitable, civic, arts and cultural organizations, Terra Group remain acutely aware of how our environment defines us, as much as we define it.</p>

            <p>For Terra Group, evolution is not a theory. It is a practice.</p>

