            <h1>RENE GONZALEZ</h1>
            <p>The Architect</p>

            <p>There’s a good chance you’ve already heard of Rene Gonzalez and his firm, Rene Gonzalez Architect. If that’s the case, then you may well share his taste for subtle contemporary details. Or the way he intentionally blurs the boundaries between the man-made and the natural landscape (what he describes as a “design conversation”). Or maybe you enjoy the tactility of his work – how his surprising choice of materials heightens your senses and makes you think about places in new and exciting ways.
Perhaps you’ve seen him mentioned in Wallpaper* magazine, The New York Times, The Wall Street Journal, Architectural Record, Architectural Digest... Or maybe you own a copy of the contemporary design book, Four Florida Moderns, and love the examples of the firm’s work that you saw. What you may not realize is that Rene has been included in more than
150 national and international publications, receiving Metropolitan Home magazine’s Home of the Year Award for design excellence in addition to being honored by Conde Nast’s House & Garden as one of 50 international designers that represent the future of design.
His unique approach has certainly not gone unnoticed by his peers: Rene is the only Miami architect to receive a National American Institute of Architecture Design Award in the last 10 years for a project in South Florida. He is also the first architect in 50 years to be twice honored for Miami projects (in 2011 for Alchemist and in 2006 for Karla). Then there are his two National AIA Honor Awards, a Florida AIA Award, and a total of ten Miami AIA Awards, including the H. Samuel Kruse Silver Medal
for Design in 2012 and the Firm of the Year award in 2011. If that wasn’t praise enough, he was twice nominated for the Cooper-Hewitt Museum’s National Design Award in Architecture.</p>
            
            <p>Then again, it is possible that you have never heard of Rene at all.  You might not know that his team was responsible for the Cisneros Fontanals Art Foundation building (a jewel in the urban landscape, with lush vegetation depicted on its glistening glass tile façade), or the two Alchemist boutiques on Lincoln Road (one perched in the sky like a floating glass cloud, the other an insulated box nestled into the ground floor). You might not have seen the home on Indian Creek that captures, filters and reflects the water, radiant sun and meditative moon, but you may well have heard that it subsequently became Miami’s most expensive residence.
</p>
<p>Whether you’re aware of Rene’s work or not, the whole of Miami has been consciously or unconsciously drawn into his ‘design conversations’ - engaging our senses, and prompting us to think differently about our surroundings.
</p>

