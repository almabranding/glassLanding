            <h1>RAYMOND JUNGLES</h1>
            <p>The Landscape Architect</p>

            <p>Words can’t really do justice to Raymond’s work. You have to see it for yourself.
You have to feel it for yourself.
After all, that is the plan.
Or rather, the design.</p>
            <p>
                From their loft-style studio overlooking the Miami River, Raymond Jungles Inc. has been meticulously bringing their sensuous outdoor spaces to life since 1981. In that time, their work has earned them 35 different design awards, and taken them to eleven different countries - including Antigua, Anguilla, the Bahamas, British Virgin Islands, China, Costa Rica, Mexico, Panama, St. Kitts and Nevis. (Which is just as well, as it’s Raymond’s inherent passion for local ecologies that informs his work).

            </p>
            <p>During his time at the University of Florida, Raymond’s vision was awakened by the work of Brazilian landscape architect, Roberto Burle Marx. And whilst Marx went on to become Raymond’s friend and mentor, RJI have continued to push the boundaries of landscape design, creating timeless, inspirational places that bring us all closer to the natural world. By posing the question “What should it be?” rather than “What could it be?” Raymond exploits (and capitalizes on) the sculptural potential of nature, allowing his elements to define spaces through their innate shapes and forms. In other words, he gives nature a platform to surprise and inspire us.
</p>
<p>
    But you really do have to see it for yourself.

</p>

